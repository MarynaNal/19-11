#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*p;

	p = (char *)s;
	while (n > 0)
	{
		p[n - 1] = '\0';
		n--;
	}
}
/*The bzero() function writes n zeroed bytes to the string s.
*/